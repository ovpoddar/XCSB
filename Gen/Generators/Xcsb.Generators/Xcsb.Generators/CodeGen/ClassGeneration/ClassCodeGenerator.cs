using System;
using System.Collections.Generic;
using System.Diagnostics;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;

namespace Xcsb.Generators.CodeGen.ClassGeneration;

internal static class ClassCodeGenerator
{
    internal static string Generate(
        INamedTypeSymbol classSymbol,
        INamedTypeSymbol interfaceSymbol,
        string methodSuffix,
        Action<StringBuilder, IMethodSymbol> writeMethodBody,
        bool addCancellationToken = false,
        bool followReturnType = false)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine();
        sb.AppendLine($"namespace {classSymbol.ContainingNamespace.ToDisplayString()}");
        sb.AppendLine("{");
        sb.AppendLine($"    {classSymbol.DeclaredAccessibility.ToString().ToLower()} partial class {classSymbol.Name}");
        sb.AppendLine("    {");

        foreach (var method in interfaceSymbol.GetMembers().OfType<IMethodSymbol>())
        {
            if (method.MethodKind != MethodKind.Ordinary) continue;
            sb.Append("        public ")
                .Append(followReturnType
                    ? method.ReturnType.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat)
                    : "void")
                .Append(" ");

            sb.Append(method.Name)
                .Append(methodSuffix);

            if (method.TypeParameters.Length > 0)
            {
                sb.Append('<');
                sb.Append(string.Join(", ", method.TypeParameters.Select(tp => tp.Name)));
                sb.Append('>');
            }

            sb.Append("(");
            for (var i = 0; i < method.Parameters.Length; i++)
            {
                var param = method.Parameters[i];
                if (i > 0) sb.Append(", ");
                sb.Append(param.Type.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat))
                    .Append(" ").Append(param.Name);
                if (param.HasExplicitDefaultValue) sb.Append(" = ").Append(param.ExplicitDefaultValue);
            }

            if (addCancellationToken && method.Parameters.Length != 0) sb.Append(", ");
            if (addCancellationToken)
                sb.Append("System.Threading.CancellationToken token = default");

            sb.Append(")");

            foreach (var tp in method.TypeParameters)
            {
                var parts = new List<string>();
                if (tp.HasReferenceTypeConstraint && !ConstrainPragmaWriter.Contain(method, "class"))
                    parts.Add("class");
                if (tp.HasValueTypeConstraint && !ConstrainPragmaWriter.Contain(method, "struct"))
                    parts.Add("struct");
                if (tp.HasUnmanagedTypeConstraint && !ConstrainPragmaWriter.Contain(method, "unmanaged"))
                    parts.Add("unmanaged");
                foreach (var ct in tp.ConstraintTypes)
                    if (!ConstrainPragmaWriter.Contain(method, ct.ToDisplayString(new SymbolDisplayFormat(
                            typeQualificationStyle: SymbolDisplayTypeQualificationStyle
                                .NameAndContainingTypesAndNamespaces)
                        )))
                        parts.Add(ct.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat));
                if (tp.HasConstructorConstraint && !ConstrainPragmaWriter.Contain(method, "new()")) parts.Add("new()");


                if (parts.Count <= 0) continue;
                sb.Append(" where ");
                sb.Append(tp.Name);
                sb.Append(" : ");
                sb.Append(string.Join(", ", parts));
            }
            ConstrainPragmaWriter.Write(sb, method, false);
            writeMethodBody(sb, method);
        }


        sb.AppendLine("    }");
        sb.AppendLine("}");
        return sb.ToString();
    }
}